import turtle
turtle.setup(800, 600)
turtle.forward(200)
turtle.backward(400)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.backward(400)
turtle.forward(200)
turtle.goto(-50, 0)
turtle.right(0)
turtle.circle(50)
turtle.done()
# It can be seen that Brutus is having an internal conflict between what Cassius told him to do and his beliefs that he holds to the interest of Rome.
